#!/usr/bin/env python3
"""Generate the README CLI reference and config reference from code.

The README's two generated sections sit between marker comments:

    <!-- BEGIN GENERATED: cli-reference -->  ...  <!-- END GENERATED: cli-reference -->
    <!-- BEGIN GENERATED: config-reference --> ... <!-- END GENERATED: config-reference -->

Everything outside the markers is hand-written and untouched.

The CLI reference is introspected from the click command tree
(``ralph_py.cli:cli``), so a command that does not exist cannot be
documented and a new command cannot be silently omitted.

The config reference is verified behaviorally, not transcribed: for
every documented ``[section]`` key the script writes a sentinel value
into a throwaway ralph.toml, runs the real loader, and asserts the
resolved config changed. It also probes every *undocumented* dataclass
field name of the per-section configs and fails if one turns out to be
a live toml key. Defaults shown in the output come from the dataclass
default instances. A key that stops working, changes its default, or
gets added without documentation makes regeneration produce a diff (or
an error), which CI catches.

Usage:
    uv run python scripts/gen_docs.py            # rewrite README.md in place
    uv run python scripts/gen_docs.py --check    # exit 1 if README.md is stale
"""

