REPO_ROOT = Path(__file__).resolve().parent.parent
README_PATH = REPO_ROOT / "README.md"

GENERATED_NOTE = "<!-- Generated by scripts/gen_docs.py - do not edit by hand. -->"


def _marker(name: str, end: bool = False) -> str:
    return f"<!-- {'END' if end else 'BEGIN'} GENERATED: {name} -->"


# ---------------------------------------------------------------------------
# CLI reference (click introspection)
# ---------------------------------------------------------------------------


def _iter_commands(
    group: click.Group, prefix: str,
) -> Iterator[tuple[str, click.Command]]:
    """Yield (full command path, command) for every leaf command, depth-first."""
    for name in sorted(group.commands):
        cmd = group.commands[name]
        path = f"{prefix} {name}"
        if isinstance(cmd, click.Group):
            yield from _iter_commands(cmd, path)
        else:
            yield path, cmd


def _usage_suffix(cmd: click.Command, path: str) -> str:
    """Return the argument part of a command's usage line (no [OPTIONS])."""
    ctx = click.Context(cmd, info_name=path)
    pieces = [p for p in cmd.collect_usage_pieces(ctx) if p != "[OPTIONS]"]
    return (" " + " ".join(pieces)) if pieces else ""


def build_cli_reference() -> str:
    from ralph_py.cli import cli

    entries: list[tuple[str, str]] = []
    for path, cmd in _iter_commands(cli, "ralph"):
        invocation = f"{path}{_usage_suffix(cmd, path)}"
        entries.append((invocation, cmd.get_short_help_str(limit=120)))

    width = max(len(invocation) for invocation, _ in entries) + 2
    lines = [f"{invocation:<{width}}{help_text}" for invocation, help_text in entries]
    body = "\n".join(lines)
    return (
        f"{GENERATED_NOTE}\n\n"
        f"```\n{body}\n```\n\n"
        "Run `ralph COMMAND --help` for the full option list of any command.\n"
    )


