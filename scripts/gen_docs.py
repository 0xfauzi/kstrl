# Sentinel values for keys whose loader validates against an enum; the
# generic type-derived sentinel would be rejected.
ENUM_SENTINELS: dict[tuple[str, str], str] = {
    ("agent", "type"): "codex",
    ("agent", "reasoning_effort"): "high",
    ("factory", "review_mode"): "advisory",
    ("security", "mode"): "hard",
    ("security", "fail_threshold"): "low",
    ("contract", "mode"): "final",
    ("knowledge", "dependency_scope"): "transitive",
    ("linear", "auth_mode"): "oauth",
}


def _sentinel_for(section: str, key: str, default: Any) -> Any:
    if (section, key) in ENUM_SENTINELS:
        return ENUM_SENTINELS[(section, key)]
    if isinstance(default, bool):
        return not default
    if isinstance(default, int):
        return default + 7919
    if isinstance(default, float):
        return default + 1234.5
    if isinstance(default, list):
        return ["sentinel/path/"]
    # str, Path, or None (str-shaped in toml)
    return "sentinel-value"


def _toml_literal(value: Any) -> str:
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)):
        return repr(value)
    if isinstance(value, list):
        return "[" + ", ".join(_toml_literal(v) for v in value) + "]"
    if value is None:
        return '""'
    if isinstance(value, Path):
        return f'"{value.as_posix()}"'
    return f'"{value}"'


@contextmanager
def _scrubbed_environ() -> Iterator[None]:
    """Run with an empty-ish environment so a developer's RALPH_* / MODEL /
    NO_COLOR etc. cannot leak into the probed loaders (env beats toml)."""
    saved = dict(os.environ)
    keep = {"PATH", "HOME", "TMPDIR", "SystemRoot"}
    try:
        for name in list(os.environ):
            if name not in keep:
                del os.environ[name]
        yield
    finally:
        os.environ.clear()
        os.environ.update(saved)


def _write_probe_toml(path: Path, specs: list[SectionSpec], extra: dict[str, list[str]]) -> None:
    lines: list[str] = []
    for spec in specs:
        lines.append(f"[{spec.section}]")
        for key, field_name in spec.keys.items():
            default = getattr(spec.defaults, field_name)
            lines.append(f"{key} = {_toml_literal(_sentinel_for(spec.section, key, default))}")
        for key in extra.get(spec.section, ()):
            lines.append(f"{key} = {_toml_literal('sentinel-value')}")
        lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


def _scalar_fields(defaults: Any) -> set[str]:
    names: set[str] = set()
    for f in dataclasses.fields(defaults):
        value = getattr(defaults, f.name)
        if value is None or isinstance(value, (bool, int, float, str, Path, list)):
            names.add(f.name)
    return names


def _verify_sections(specs: list[SectionSpec]) -> None:
    """Behavioral check: every documented key changes the resolved config;
    no undocumented scalar field of a per-section config is a live toml key."""
    errors: list[str] = []
    extra: dict[str, list[str]] = {}
    for spec in specs:
        if spec.probe_undocumented_fields:
            candidates = _scalar_fields(spec.defaults) - set(spec.keys.values())
            # None-defaulted non-scalar handling: only str-shaped probes are
            # meaningful; every current config field is scalar or a sub-config.
            extra[spec.section] = sorted(candidates)

    with _scrubbed_environ(), tempfile.TemporaryDirectory() as tmp:
        # Baseline and probe load from the SAME root, differing only in
        # whether ralph.toml exists: several configs resolve path fields
        # against root_dir, so loads from two different roots would
        # spuriously differ on fields the toml never touched.
        probe_root = Path(tmp) / "probe"
        probe_root.mkdir()
        toml_path = probe_root / "ralph.toml"

        baselines = {spec.section: spec.loader(probe_root) for spec in specs}
        _write_probe_toml(toml_path, specs, extra)

        for spec in specs:
            baseline = baselines[spec.section]
            probed = spec.loader(probe_root)
            for key, field_name in spec.keys.items():
                if getattr(probed, field_name) == getattr(baseline, field_name):
                    errors.append(
                        f"[{spec.section}] {key}: documented but setting it in "
                        f"ralph.toml did not change {type(baseline).__name__}.{field_name}"
                    )
            for field_name in extra.get(spec.section, ()):
                if getattr(probed, field_name) != getattr(baseline, field_name):
                    errors.append(
                        f"[{spec.section}] {field_name}: live toml key not documented "
                        "in scripts/gen_docs.py (add it to the section spec + description)"
                    )
            for key in spec.keys:
                if (spec.section, key) not in KEY_DESCRIPTIONS:
                    errors.append(f"[{spec.section}] {key}: missing KEY_DESCRIPTIONS entry")

    if errors:
        raise SystemExit("gen_docs config probing failed:\n  " + "\n  ".join(errors))


def build_config_reference() -> str:
    specs = _section_specs()
    _verify_sections(specs)

    blocks: list[str] = []
    for spec in specs:
        lines = [f"# {spec.title}", f"[{spec.section}]"]
        width = max(
            len(f"{key} = {_toml_literal(getattr(spec.defaults, field_name))}")
            for key, field_name in spec.keys.items()
        ) + 2
        for key, field_name in spec.keys.items():
            assignment = f"{key} = {_toml_literal(getattr(spec.defaults, field_name))}"
            description = KEY_DESCRIPTIONS[(spec.section, key)]
            lines.append(f"{assignment:<{width}}# {description}")
        blocks.append("\n".join(lines))

    body = "\n\n".join(blocks)
    return (
        f"{GENERATED_NOTE}\n"
        "<!-- Defaults come from the config dataclasses; every key is probed\n"
        "     against the real loaders before this section is emitted. -->\n\n"
        f"```toml\n{body}\n```\n\n"
        "Environment variables override ralph.toml, and CLI flags override both.\n"
        "See [docs/env-vars.md](docs/env-vars.md) for the full env-var mapping.\n"
    )


# ---------------------------------------------------------------------------
# README splicing
# ---------------------------------------------------------------------------


def _splice(text: str, name: str, generated: str) -> str:
    begin, end = _marker(name), _marker(name, end=True)
    if begin not in text or end not in text:
        raise SystemExit(f"README.md is missing the {begin} / {end} markers")
    head, rest = text.split(begin, 1)
    _, tail = rest.split(end, 1)
    return f"{head}{begin}\n{generated}{end}{tail}"


def render_readme(text: str) -> str:
    text = _splice(text, "cli-reference", build_cli_reference())
    text = _splice(text, "config-reference", build_config_reference())
    return text


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check", action="store_true",
        help="verify README.md is current; exit 1 on drift without writing",
    )
    args = parser.parse_args(argv)

    current = README_PATH.read_text(encoding="utf-8")
    rendered = render_readme(current)

    if args.check:
        if rendered != current:
            sys.stderr.write(
                "README.md generated sections are stale.\n"
                "Run: uv run python scripts/gen_docs.py\n"
            )
            return 1
        print("README.md generated sections are current.")
        return 0

    if rendered != current:
        README_PATH.write_text(rendered, encoding="utf-8")
        print("README.md updated.")
    else:
        print("README.md already current.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
